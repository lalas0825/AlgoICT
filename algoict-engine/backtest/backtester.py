    @staticmethod
    def _tf_delta(tf: str) -> pd.Timedelta:
        """Bar duration for a timeframe label."""
        mapping = {
            "1min": pd.Timedelta(minutes=1),
            "5min": pd.Timedelta(minutes=5),
            "15min": pd.Timedelta(minutes=15),
            "1H": pd.Timedelta(hours=1),
            "4H": pd.Timedelta(hours=4),
            "D": pd.Timedelta(hours=23),
            "W": pd.Timedelta(days=5),
        }
        return mapping.get(tf, pd.Timedelta(minutes=1))

    @staticmethod
    def _latest_closed_idx(
        ts_index: pd.DatetimeIndex,
        current_ts: pd.Timestamp,
        delta: pd.Timedelta,
    ) -> int:
        """
        Index of the most recent bar whose close-time <= current_ts.

        A bar labeled T with duration delta closes at T + delta. Return the
        largest idx where ts_index[idx] <= current_ts - delta.  Returns -1
        if no bar has closed yet.
        """
        cutoff = current_ts - delta
        idx = int(ts_index.searchsorted(cutoff, side="right")) - 1
        return idx

    @staticmethod
    def _filter_dates(
        df: pd.DataFrame,
        start_date: Optional[str],
        end_date: Optional[str],
    ) -> pd.DataFrame:
        """Date-range filter. Inputs are 'YYYY-MM-DD' strings in US/Central."""
        if df.empty:
            return df
        tz = df.index.tz
        if start_date:
            df = df[df.index >= pd.Timestamp(start_date, tz=tz)]
        if end_date:
            end_ts = pd.Timestamp(end_date, tz=tz) + pd.Timedelta(days=1)
            df = df[df.index < end_ts]
        return df

