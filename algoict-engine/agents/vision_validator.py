    def _parse_response(
        self,
        response_text: str,
        context: dict,
        elapsed_ms: int,
        images_used: int,
    ) -> VisionValidatorDecision:
        kz = context.get("kz", "unknown")
        clean = response_text.strip()
        # Strip code fences if Claude added them
        if clean.startswith("```"):
            m = re.match(r"^```(?:json)?\s*\n(.*?)\n```\s*$", clean, re.S)
            if m:
                clean = m.group(1).strip()
            else:
                clean = clean.split("\n", 1)[1] if "\n" in clean else clean

        try:
            data = json.loads(clean)
            decision = str(data.get("decision", "fire")).lower().strip()
            if decision not in _VALID_DECISIONS:
                logger.warning(
                    "VisionValidator: invalid decision '%s' for %s; falling back",
                    decision, kz,
                )
                decision = "fire"
            default_mult = {"fire": 1.0, "half": 0.5, "skip": 0.0}[decision]
            size_mult = float(data.get("size_multiplier", default_mult))
            confidence = float(data.get("confidence", 0.5))
            confidence = max(0.0, min(1.0, confidence))
            rationale = str(data.get("rationale", ""))[:500]

            # 2026-05-21 — bot_assessment: independent evaluation of whether
            # the bot is reading the chart correctly. Always parse defensively;
            # use empty dict if missing or malformed.
            bot_assessment = {}
            ba_raw = data.get("bot_assessment", {})
            if isinstance(ba_raw, dict):
                valid_assessments = {"accurate", "questionable", "wrong", "missing"}
                for k in ("fvg_assessment", "sweep_assessment", "mss_assessment"):
                    v = str(ba_raw.get(k, "")).lower().strip()
                    if v in valid_assessments:
                        bot_assessment[k] = v
                    elif v:
                        # Unknown value — log but don't crash
                        logger.debug(
                            "bot_assessment %s invalid value: %s", k, v,
                        )
                        bot_assessment[k] = v[:30]  # store truncated for analysis
                overall = ba_raw.get("overall")
                if overall:
                    bot_assessment["overall"] = str(overall)[:300]

            return VisionValidatorDecision(
                kz=kz,
                decision=decision,
                size_multiplier=size_mult,
                confidence=confidence,
                rationale=rationale,
                model=self._model,
                response_ms=elapsed_ms,
                images_used=images_used,
                context=context,
                bot_assessment=bot_assessment,
            )
        except Exception as exc:
            logger.error(
                "VisionValidator parse failed for %s: %s\nResponse[:300]: %s",
                kz, exc, response_text[:300],
            )
            return VisionValidatorDecision(
                kz=kz, decision="fire", size_multiplier=1.0,
                confidence=0.0, rationale=f"Parse error: {exc}",
                model=self._model, response_ms=elapsed_ms,
                images_used=images_used, context=context, error=str(exc),
            )


