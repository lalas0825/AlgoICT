"""
agents/vision_validator.py
===========================
Vision-overlay AI validator (Camino C4, SHADOW mode).

2026-05-20 — Builds on Camino C2 text-only validator. The key insight:
text-only C2 reads CONTEXT generated by the bot's own detectors, so
if the bot's data is wrong, Claude inherits the error (circular trust).

Vision-overlay shows Claude the raw candles WITH the bot's annotations
overlaid. Claude can independently validate:
  - Does the FVG zone the bot detected actually look like a clean FVG?
  - Is the sweep clean or a weak pivot?
  - Does the 5-min structure (MSS/BOS) appear on the chart?
  - Is the broader regime supportive (trend/range/chop)?

This breaks the "bot data feeding Claude" circular validation problem.

Inputs:
  - Text context (same as C2)
  - 1-min PNG chart (last ~90 bars) with bot annotations + proposed levels
  - 5-min PNG chart (last ~60 bars) for HTF context

Output: same JSON shape as C2 — {decision, size_multiplier, confidence, rationale}

In SHADOW mode, decision is logged to JSONL but bot continues canonical
strategy. Phase 2 active mode would gate trades based on decision.

Cost ~$0.017/call (Sonnet with 2 images), 3-6 sec latency. At ~5 unique
signals/day (after dedup), ~$0.10/day = $3/month.
"""

